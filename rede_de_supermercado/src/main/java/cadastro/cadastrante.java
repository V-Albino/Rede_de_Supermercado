        InserirCadastrante.setCpf(jTextFieldCpf.getText());
        InserirCadastrante.setNome(jTextFieldNome.getText());
        InserirCadastrante.setEmail(jTextFieldEmail.getText());
        InserirCadastrante.setTelefone(jTextFieldTelefone.getText());
